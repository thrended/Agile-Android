    fun matchesSearchQuery(que: String): Boolean {
        if (que.isBlank()) return false
        val matching = listOfNotNull(
            email, phone, uid,
            email?.split("@")?.first(), "${email?.split("@")?.first()?.first()} ${email?.split("@")?.last()?.first()}",
            email?.split("@")?.last(), "id: $id", "id:$id",
            "${name?.split(" ")?.first()?.first()}${name?.split(" ")?.last()?.first()}",
            "${name?.split(" ")?.first()?.first()} ${name?.split(" ")?.last()?.first()}",
            name?.split(" ")?.first(), name?.split(" ")?.last(),
            "${firstName?.split(" ")?.first()?.first()} ${lastName?.split(" ")?.last()?.first()}",
            "${firstName?.first()} ${middleName?.first()} ${lastName?.first()}", "${firstName?.first()}${middleName?.first()}${lastName?.first()}",
            "${firstName?.first()} ${lastName?.first()}", "${firstName?.first()}${lastName?.first()}",
            "adm=$isAdmin", "adm: $isAdmin", "admin=$isAdmin", "admin: $isAdmin", "ver=$isVerified",
            "dis=$isDisabled", "pwr=$isPowerUser", "off=$isOfflineMode", "on=$isOnline"
        )
        return matching.any {
            it.contains(que, ignoreCase = true)
        }
    }
